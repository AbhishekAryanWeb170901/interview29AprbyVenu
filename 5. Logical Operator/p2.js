// Switch Statement
//Break Statement

var choice = 5;


switch(choice)
{
  case 1 : console.log("Code 1");
           break;

  case 2 : console.log("Code 2");
           break;

  case 3 : console.log("Code 3");
           break;

  default : console.log("Wrong Choice");
            break;

} 






